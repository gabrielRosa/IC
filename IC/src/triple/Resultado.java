public class Resultado {
    
    final Double simLch;
    final Double simWup;
    final Double simRes;
    final Double simLin;
    final Double simJnc;

    public Resultado(Double simLch, Double simWup, Double simRes, Double simLin, Double simJnc) {
        this.simLch = simLch;
        this.simWup = simWup;
        this.simRes = simRes;
        this.simLin = simLin;
        this.simJnc = simJnc;
    }
    
}
